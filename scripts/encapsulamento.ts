/* -Encapsulamento nod permite definir diferentes níveis de acesso para nossos atributos e métodos;
-Usando os modificadores de acesso, podemos determinar até que ponto um atributo ou método é visível para
outras variáveis ou classes.
-Nos ajuda a proteger a integridade das informações de nossa classe;
-Existem 4 --modificadores de acesso-- que podemos atribuir aos nossos atributos e 3 --aos métodos;

1 - public (podem acessar ou alterar os atributos e métodos que estão na classe por qualquer outra classe)
    Ex: banheiro público;

2 - protected (Atributos e métodos protegidos podem ser acessados ou alterados por meio da classe em que foram
    criados e por meio das classes que forem filhas dela).

    Ex:banheiro domiciliar;

3 - private (Atributos e métodos privados são os mais restritos. Eles podem ser acessados ou alterados 
    apenas por meio da classe em que foram criados).

    Ex:banheiro de uma suíte;

4 - readonly (*apenas para atributos*, atributo que podem apenas serem vistos ou lidos, mas não podem ser alterados).

    Ex: banheiro exposto em loja;
*/
